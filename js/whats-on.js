const displayMovieDetails = (day) =>{
    let element = document.getElementById("movieDetails");
    element.innerHTML="";
    let content = "";
    for(movie of movieDetail){
        let openingDiv = `<div class="movie">`;
        let openingTimeDiv = `<div class="movieTime">`;
        let animationName = `<h3>${movie.animation}</h3>`
        let movieTime = `<p>${movie.time}</p>`;
        let openingDetailsDiv = `<div class="movieDetails">`;
        let closingDiv = `</div>`
        let movieDate = "";
        let movieTrailer = "";
        let movieName = "";
        let directorName = "";
        let synopsis = "";
        let genre = "";
        if(day==="day1")
        {
            movieDate = `<p>${movie.day1.date}</p>`;
            movieTrailer = `<iframe class="trailer" src=${movie.day1.trailerURL} ></iframe>`;
            movieName = `<h2>${movie.day1.name}</h2>`;
            directorName = `<p><b>Director:</b> ${movie.day1.director}</p>`;
            genre = `<p><b>Genre:</b> ${movie.day1.genre}</p>`;
            synopsis = `<p class="synopsis">${movie.day1.synopsis}</p>`;
            document.getElementById("Day1").style.backgroundColor = "#ffa8d7";
            document.getElementById("Day2").style.removeProperty('background-color');
            document.getElementById("Day3").style.removeProperty('background-color');
        }
        else if(day==="day2")
        {
            movieDate = `<p>${movie.day2.date}</p>`;
            movieTrailer = `<iframe class="trailer" src=${movie.day2.trailerURL} ></iframe>`;
            movieName = `<h2>${movie.day2.name}</h2>`;
            directorName = `<p><b>Director:</b> ${movie.day2.director}</p>`;
            genre = `<p><b>Genre:</b> ${movie.day2.genre}</p>`;
            synopsis = `<p class="synopsis">${movie.day2.synopsis}</p>`;
            document.getElementById("Day2").style.backgroundColor = "#ffa8d7";
            document.getElementById("Day1").style.removeProperty('background-color');
            document.getElementById("Day3").style.removeProperty('background-color');
        }
        else
        {
            movieDate = `<p>${movie.day3.date}</p>`;
            movieTrailer = `<iframe class="trailer" src=${movie.day3.trailerURL} ></iframe>`;
            movieName = `<h2>${movie.day3.name}</h2>`;
            directorName = `<p><b>Director:</b> ${movie.day3.director}</p>`;
            genre = `<p><b>Genre:</b> ${movie.day3.genre}</p>`;
            synopsis = `<p class="synopsis">${movie.day3.synopsis}</p>`;
            document.getElementById("Day3").style.backgroundColor = "#ffa8d7";
            document.getElementById("Day1").style.removeProperty('background-color');
            document.getElementById("Day2").style.removeProperty('background-color');
        }
        content+=openingDiv+openingTimeDiv+animationName+movieDate+movieTime+closingDiv+movieTrailer+openingDetailsDiv+movieName+directorName+genre+synopsis+closingDiv+closingDiv;
    }
    element.innerHTML = content;
}

